function createHeader() {
    let header = document.querySelector('.header');
    header.innerHTML = `
        <h1>BTS Merchandise Store</h1>
        <nav>
            <ul>
                <li><a href="#home">Beranda</a></li>
                <li><a href="#products">Produk</a></li>
                <li><a href="#contact">Contact</a></li>
            </ul>
        </nav>
    `;
}

function createHomePage() {
    let content = document.querySelector('.content');
    let homeSection = document.createElement('section');
    homeSection.id = 'home';
    homeSection.innerHTML = `
        <div class="home-banner">
            <h2>Welcome to BTS Merchandise Store</h2>
            <p>Your one-stop shop for all official BTS merchandise.</p>
            <button id="shop-now">Shop Now</button>
        </div>
    `;
    content.appendChild(homeSection);
    document.getElementById('shop-now').addEventListener('click', () => {
        window.location.href = '#products';
    });
}

function card(product) {
    let content = document.querySelector('.product-grid');
    let poductSection = document.createElement('section');
    poductSection.id = 'products';
    let figure = document.createElement('figure');
    let figcaption = document.createElement('figcaption');
    let h1 = document.createElement('h1');
    h1.textContent = product.name;
    let img = document.createElement('img');
    img.src = product.image;
    let h2 = document.createElement('h2');
    h2.textContent = product.price;
    let ul = document.createElement('ul');
    let h4 = document.createElement('h4');
    h4.textContent = "Detail Product :";
    product.detail.map((d) => {
        let li = document.createElement('li');
        li.textContent = d;
        ul.appendChild(li);
    })
    let button1 = document.createElement('button');
    button1.textContent = "Beli sekarang";
    button1.addEventListener('click', () => {
        confirm('Apakah anda yakin ingin beli produk ini?');
    })
    let button2 = document.createElement('button');
    button2.textContent = "Masukkan Ke Keranjang";
    button2.addEventListener('click', () => {
        alert('Produk dimasukkan ke keranjang anda');
    })
    let button3 = document.createElement('button');
    button3.textContent = "Coment";
    button3.addEventListener('click', () => {
        prompt("Silahkan isi komentar anda");
    })
    figcaption.appendChild(h1);
    figcaption.appendChild(h2);
    figcaption.appendChild(h4);
    figcaption.appendChild(ul);
    figcaption.appendChild(button1);
    figcaption.appendChild(button2);
    figcaption.appendChild(button3);
    figure.appendChild(img);
    figure.appendChild(figcaption);
    content.appendChild(poductSection);
    content.appendChild(figure);

}

